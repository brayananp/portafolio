/**
 * pdf-lib drawSvgPath uses `y` as the top edge of the icon (SVG origin).
 * Align the icon vertically with the visual center of the text line.
 */
export function getIconTopY(
	textBaselineY: number,
	font: PDFFont,
	textSize: number,
	iconSize: number,
): number {
	const textCenterY = textBaselineY + font.heightAtSize(textSize) * 0.42;
	return textCenterY + iconSize / 2;
}

export function drawContactIcon(
	page: PDFPage,
	type: ContactIconType,
	x: number,
	textBaselineY: number,
	iconSize: number,
	font: PDFFont,
	textSize: number,
	color: RGB,
): void {
	page.drawSvgPath(iconPaths[type], {
		x,
		y: getIconTopY(textBaselineY, font, textSize, iconSize),
		scale: iconSize / ICON_VIEWBOX,
		color,
	});
}
